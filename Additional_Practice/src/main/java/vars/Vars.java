package vars;

public class Vars {

	public static void main(String[] args) {
		var x = 100; // int
		var y = 1.90; // double
		var z = 'a'; // char
		var p = "tanu"; // string
		var q = false; // boolean

		// automatically detects the datatype of a variable on compile time
		// Cannot change type later.-
		System.out.println(x);
		System.out.println(y);
		System.out.println(z);
		System.out.println(p);
		System.out.println(q);
	}
}
